//Searchbar Query
const form = document.querySelector('form');
form.addEventListener('submit', function (event) {
    event.preventDefault();
    const searchQuery = document.querySelector('#search').value.toLowerCase();
    if (searchQuery === 'about') {
        window.location.href = 'about.html';
    } else if (searchQuery === 'service') {
        window.location.href = 'listing-page.html';
    } else {
        window.location.href = 'search-results.html?query=' + searchQuery;
    }
});

//Sidemenu
let arrow = document.querySelectorAll(".arrow");
for (var i = 0; i < arrow.length; i++) {
    arrow[i].addEventListener("click", (e) => {
        let arrowParent = e.target.parentElement.parentElement;//selecting main parent of arrow
        arrowParent.classList.toggle("showMenu");
    });
}
